    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $sub = Subject::updateOrCreate(['id' => $request->id], $request->except('_token'));
        if ($sub->count() > 0) {
            ExamSetting::create(['subject_id' => $sub->id, 'number_of_question' => 0]);
            return response()->json(['type' => 'success', 'message' => 'Done!']);
        }
        return response()->json(['type' => 'error', 'message' => 'Error!']);
    }

